package com.java.pilacola;

/**
 *
 * @author liamy
 */
public class Estudiante implements Identificable {
    int codigo;
    String nombre;

    public String getId(){
        return String.valueOf(codigo);
    }
    
    public Estudiante(int codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
